    </div>
    <div class="p-6">
        <h5 class="text-xl text-green-600 font-semibold mb-2">Pengantar</h5>
        <p class="mb-4">Dalam bahasa Inggris, kata-kata dikelompokkan menjadi beberapa kategori berdasarkan fungsi dan peran mereka dalam kalimat. Kategori ini dikenal sebagai "parts of speech". Ada delapan jenis utama parts of speech, yaitu:</p>
        <ol class="list-decimal list-inside pl-4 mb-6">
            <li class="py-1">Noun (Kata Benda)</li>
            <li class="py-1">Pronoun (Kata Ganti)</li>
            <li class="py-1">Adjective (Kata Sifat)</li>
            <li class="py-1">Verb (Kata Kerja)</li>
            <li class="py-1">Adverb (Kata Keterangan)</li>
            <li class="py-1">Preposition (Kata Depan)</li>
            <li class="py-1">Conjunction (Kata Penghubung)</li>
            <li class="py-1">Interjection (Kata Seru)</li>
        </ol>

        <h5 class="text-xl text-green-600 font-semibold mb-2">Penjelasan dan Contoh</h5>

        <h6 class="text-lg text-blue-700 font-semibold mb-1">1. Noun (Kata Benda)</h6>
        <p class="mb-2">Noun adalah kata yang digunakan untuk menyebut orang, tempat, benda, atau konsep.</p>
        <ul class="list-disc list-inside pl-4 mb-4">
            <li class="py-1"><strong>People (Orang):</strong> John, teacher, mother</li>
            <li class="py-1"><strong>Places (Tempat):</strong> school, city, park</li>
            <li class="py-1"><strong>Things (Benda):</strong> book, car, apple</li>
            <li class="py-1"><strong>Concepts (Konsep):</strong> love, freedom, happiness</li>
        </ul>

        <h6 class="text-lg text-blue-700 font-semibold mb-1">2. Pronoun (Kata Ganti)</h6>
        <p class="mb-2">Pronoun adalah kata yang digunakan untuk menggantikan noun. Pronoun digunakan untuk menghindari pengulangan noun yang sama.</p>
        <ul class="list-disc list-inside pl-4 mb-4">
            <li class="py-1"><strong>I, you, he, she, it, we, they</strong></li>
        </ul>
        <p class="mt-2 mb-2">Contoh kalimat:</p>
        <ul class="list-disc list-inside pl-4 mb-4">
            <li class="py-1">John is a teacher. <strong>He</strong> teaches math.</li>
            <li class="py-1">Lisa has a book. <strong>It</strong> is interesting.</li>
        </ul>

        <h6 class="text-lg text-blue-700 font-semibold mb-1">3. Adjective (Kata Sifat)</h6>
        <p class="mb-2">Adjective adalah kata yang digunakan untuk mendeskripsikan atau memberikan informasi lebih lanjut tentang noun atau pronoun.</p>
        <ul class="list-disc list-inside pl-4 mb-4">
            <li class="py-1"><strong>big, small, beautiful, interesting</strong></li>
        </ul>
        <p class="mt-2 mb-2">Contoh kalimat:</p>
        <ul class="list-disc list-inside pl-4 mb-4">
            <li class="py-1">She has a <strong>big</strong> house.</li>
            <li>The <strong>beautiful</strong> garden is full of flowers.</li>
        </ul>

        <h6 class="text-lg text-blue-700 font-semibold mb-1">4. Verb (Kata Kerja)</h6>
        <p class="mb-2">Verb adalah kata yang menggambarkan tindakan, keadaan, atau keberadaan.</p>
        <ul class="list-disc list-inside pl-4 mb-4">
            <li class="py-1"><strong>run, eat, is, have</strong></li>
        </ul>
        <p class="mt-2 mb-2">Contoh kalimat:</p>
        <ul class="list-disc list-inside pl-4 mb-4">
            <li class="py-1">He <strong>runs</strong> every morning.</li>
            <li class="py-1">She <strong>is</strong> a student.</li>
        </ul>

        <h6 class="text-lg text-blue-700 font-semibold mb-1">5. Adverb (Kata Keterangan)</h6>
        <p class="mb-2">Adverb adalah kata yang mendeskripsikan verb, adjective, atau adverb lain. Adverb memberikan informasi lebih lanjut tentang bagaimana, kapan, di mana, atau seberapa sering suatu tindakan terjadi.</p>
        <ul class="list-disc list-inside pl-4 mb-4">
            <li class="py-1"><strong>quickly, very, well, yesterday</strong></li>
        </ul>
        <p class="mt-2 mb-2">Contoh kalimat:</p>
        <ul class="list-disc list-inside pl-4 mb-4">
            <li class="py-1">He runs <strong>quickly</strong>.</li>
            <li class="py-1">She is <strong>very</strong> smart.</li>
        </ul>

        <h6 class="text-lg text-blue-700 font-semibold mb-1">6. Preposition (Kata Depan)</h6>
        <p class="mb-2">Preposition adalah kata yang menunjukkan hubungan antara noun atau pronoun dengan kata lain dalam kalimat.</p>
        <ul class="list-disc list-inside pl-4 mb-4">
            <li class="py-1"><strong>in, on, at, under, between</strong></li>
        </ul>
        <p class="mt-2 mb-2">Contoh kalimat:</p>
        <ul class="list-disc list-inside pl-4 mb-4">
            <li class="py-1">The book is <strong>on</strong> the table.</li>
            <li class="py-1">She is sitting <strong>between</strong> John and Lisa.</li>
        </ul>

        <h6 class="text-lg text-blue-700 font-semibold mb-1">7. Conjunction (Kata Penghubung)</h6>
        <p class="mb-2">Conjunction adalah kata yang menghubungkan kata, frasa, atau klausa.</p>
        <ul class="list-disc list-inside pl-4 mb-4">
            <li class="py-1"><strong>and, but, or, because, although</strong></li>
        </ul>
        <p class="mt-2 mb-2">Contoh kalimat:</p>
        <ul class="list-disc list-inside pl-4 mb-4">
            <li class="py-1">I want to go to the party, <strong>but</strong> I have to study.</li>
            <li class="py-1">She likes apples <strong>and</strong> oranges.</li>
        </ul>

        <h6 class="text-lg text-blue-700 font-semibold mb-1">8. Interjection (Kata Seru)</h6>
        <p class="mb-2">Interjection adalah kata atau frasa yang digunakan untuk mengekspresikan emosi atau reaksi yang kuat.</p>
        <ul class="list-disc list-inside pl-4 mb-4">
            <li class="py-1"><strong>oh, wow, ouch, hey</strong></li>
        </ul>
        <p class="mt-2 mb-2">Contoh kalimat:</p>
        <ul class="list-disc list-inside pl-4 mb-4">
            <li class="py-1"><strong>Wow</strong>, that's amazing!</li>
            <li class="py-1"><strong>Ouch</strong>, that hurts!</li>
        </ul>

        <h5 class="text-xl text-green-600 font-semibold mb-2 mt-4">Kesimpulan</h5>
        <p>Memahami parts of speech sangat penting dalam belajar bahasa Inggris karena membantu kita untuk membentuk kalimat yang benar dan efektif. Dengan mengetahui fungsi dan peran setiap parts of speech, kita dapat meningkatkan kemampuan berbicara, menulis, dan memahami bahasa Inggris dengan lebih baik.</p>
